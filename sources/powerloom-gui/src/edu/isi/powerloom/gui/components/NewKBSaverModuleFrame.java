    void setupModuleList() {
	try {
            PLSurrogateContainer modules = KnowledgeManager.getInstance().getModules();
            moduleList.setModel(new PLListModel(modules.listifyTreeContainer()));
	} catch (Exception e) {
	    PowerloomApp.getInstance().handleException(e);
	}        
    }
    
