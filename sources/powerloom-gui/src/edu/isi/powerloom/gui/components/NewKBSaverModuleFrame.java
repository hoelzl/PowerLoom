    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        PLModule selectedModule = (PLModule)moduleList.getSelectedValue();
        if (selectedModule == null) {
	    JOptionPane.showMessageDialog(PowerloomApp.getInstance(), "You must select a module before you proceed.", "Select Module", JOptionPane.WARNING_MESSAGE);
	    return;
        } else {
	    if (isLocal) {
		PLLocalFileBrowser fileBrowser = new PLLocalFileBrowser(PowerloomApp.getInstance(), selectedModule);
		fileBrowser.showSaveKBDialog();
	    } else {
                // disabled for now/ever (since we moved all the remote file support to attic
                // since it was a major security hole and used Sun proprietary source code).
		//PLRemoteFileBrowser fileBrowser = new PLRemoteFileBrowser(PowerloomApp.getInstance(), selectedModule);
		//fileBrowser.showSaveKBDialog();
	    }
        }
        setVisible(false);
    }//GEN-LAST:event_NextButtonActionPerformed
    
    
